	<h3 class="h3 w-full mt-4">Filter:</h3>

	<p class="mt-8 mx-auto">
        Der neue Filter ermöglicht es, schnell und effizient nach Figuren zu suchen. Einfach den Namen oder die Serie einer Figur als Text eingeben, um eine normale Suche durchzuführen. Wenn Sie speziell nach einer Serie suchen möchten, setzen Sie einfach ein <code class="bg-gray-200 text-gray-700 p-1 rounded">#</code> vor die Suche.
        <br>
        Beispiel:
        <code class="bg-gray-200 text-gray-700 p-1 rounded">#EN</code>
        <br>
        Oder wenn Sie gezielt nach einer Figur mithilfe der MPG-Nummer suchen möchten, setzen Sie ebenfalls ein <code class="bg-gray-200 text-gray-700 p-1 rounded">#</code> vor die Suche.
        <br>
        Beispiel:
        <code class="bg-gray-200 text-gray-700 p-1 rounded">#EN202</code>
        <br>
        Der Filter bietet weitere Funktionen, die durch Klicken auf <code class="bg-gray-200 text-gray-700 p-1 rounded">Filter ^</code> sichtbar werden. Dazu gehören die Suche nach der aktuellen Serie<br> <code class="bg-gray-200 text-gray-700 p-1 rounded">Aktuelle Serie</code>, die Suche nach Maxi-Figuren <code class="bg-gray-200 text-gray-700 p-1 rounded">Maxi</code>, die Suche nach einem Zeitraum über den Slider. Es ist wichtig zu beachten, dass diese Optionen frei miteinander kombinierbar sind.
    </p>
